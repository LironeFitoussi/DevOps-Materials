#!/usr/bin/env python3
# Same code with functions - clean and reusable

def calculate_area(length, width):
    return length * width

# Calculate area of rectangle 1
area1 = calculate_area(10, 5)
print(f"Rectangle 1 area: {area1}")

# Calculate area of rectangle 2
area2 = calculate_area(8, 6)
print(f"Rectangle 2 area: {area2}")

# Calculate area of rectangle 3
area3 = calculate_area(12, 4)
print(f"Rectangle 3 area: {area3}")

# Much cleaner and easier to maintain!
