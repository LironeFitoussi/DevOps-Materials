#!/usr/bin/env python3
# Creating dictionaries

# Empty dictionary
empty = {}
print(f"Empty: {empty}")

# Dictionary with key-value pairs
user = {
    "name": "Alice",
    "age": 25,
    "city": "Boston"
}
print(f"User: {user}")

# Dictionary with different value types
config = {
    "host": "localhost",
    "port": 8080,
    "secure": True,
    "timeout": 30.5
}
print(f"Config: {config}")
